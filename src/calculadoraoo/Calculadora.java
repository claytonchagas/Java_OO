package calculadoraoo;

public class Calculadora {
	public double adicao(double a, double b){
		double r = a + b;
		return r;
	}

	public double subtracao(double a, double b) {
		return a - b;
	}

	public double divisao(double c, double d) {
		double r;
		r = c / d;
		return r;
	}

	public double multiplicacao(double c, double d) {
		double r = c * d;
		return r;
	}
}